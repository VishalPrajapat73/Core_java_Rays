package in.coTestInheritance;

public class Person {
		private String name;
		private int Age;
		
		public Person(String name, int Age) {
			this.name = name;
			this.Age = Age;
		}
		public String getname() {
			return name;
		}
		public int getage() {
			return Age;
		}
		
		
		
}
